/**
 * Returns a fallback image URL for a specific category
 * Uses simple string hashing of article identifier to properly distribute images
 */
export const getCategoryFallbackImage = (category, identifier = '') => {
    // Determine the index based on identifier (title/ID) to be deterministic
    let index = 0;

    // Simple string hash
    if (identifier && typeof identifier === 'string') {
        for (let i = 0; i < identifier.length; i++) {
            index += identifier.charCodeAt(i);
        }
    } else if (typeof identifier === 'number') {
        index = identifier;
    } else {
        // Random fallback if no identifier
        index = Math.floor(Math.random() * 100);
    }

    if (!category) return genericFallbacks[index % genericFallbacks.length];

    // Normalize category string
    const key = category.toLowerCase().trim();

    // Check direct match
    if (categoryImages[key]) {
        return categoryImages[key][index % categoryImages[key].length];
    }

    // Check included key
    const partialMatch = Object.keys(categoryImages).find(k => key.includes(k));
    if (partialMatch) {
        return categoryImages[partialMatch][index % categoryImages[partialMatch].length];
    }

    // Return generic
    return genericFallbacks[index % genericFallbacks.length];
};
